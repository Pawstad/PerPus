    private void loadBukuKeList() {
    DefaultListModel<String> modelTersedia = new DefaultListModel<>();
    DefaultListModel<String> modelDipinjam = new DefaultListModel<>();

    List<Buku> semuaBuku = bukuService.getAllBooks();

    for (Buku buku : semuaBuku) {
        String entry = String.format("%s - %s (%s, %d)", 
            buku.getId(), 
            buku.getJudul(), 
            buku.getPenulis(), 
            buku.getTahunTerbit());

        if (buku.isAvailable()) {
            modelTersedia.addElement(entry);
        } else {
            String namaPeminjam = peminjamanService.getNamaPeminjam(buku.getId());
            if (namaPeminjam != null) {
                entry += " | Dipinjam oleh: " + namaPeminjam;
            }
            modelDipinjam.addElement(entry);
        }
    }

    listBukuTersedia.setModel(modelTersedia);
    listBukuDipinjam.setModel(modelDipinjam);
}


    private void pasangEventKlikList() {
    listBukuTersedia.addListSelectionListener(e -> {
        if (!e.getValueIsAdjusting()) {
            String selected = listBukuTersedia.getSelectedValue();
            if (selected != null && selected.contains(" - ")) {
                String idBuku = selected.split(" - ")[0].trim();
                fieldIdBuku.setText(idBuku);
            }
        }
    });

    listBukuDipinjam.addListSelectionListener(e -> {
        if (!e.getValueIsAdjusting()) {
            String selected = listBukuDipinjam.getSelectedValue();
            if (selected != null && selected.contains(" - ")) {
                String idBuku = selected.split(" - ")[0].trim();
                fieldIdBuku.setText(idBuku);
            }
        }
    });
}



