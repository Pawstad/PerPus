    private void btnPinjamBukuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPinjamBukuActionPerformed
        String selected = listBukuTersedia.getSelectedValue();
    if (selected == null) return;

    // Ambil ID buku dari string, misal ID buku ada di awal
    String idBuku = selected.split(" - ")[0];

    // Misalnya Anda punya nama user dari TextField
    String namaUser = fieldNamaUser.getText().trim(); 
    if (namaUser.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Masukkan nama peminjam!");
        return;
    }

    // Tandai buku sebagai tidak tersedia
    bukuService.setAvailability(idBuku, false);

    // Simpan ke daftar peminjaman
    peminjamanService.tambahPeminjaman(idBuku, namaUser);

    // Reload tampilan
    loadBukuKeList();
    }//GEN-LAST:event_btnPinjamBukuActionPerformed

    private void btnKembalikanBukuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKembalikanBukuActionPerformed
        String idBuku = fieldIdBuku.getText().trim();

    if (idBuku.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Masukkan ID Buku yang ingin dikembalikan.");
        return;
    }

    boolean sukses = peminjamanService.kembalikanBuku(idBuku);
    if (sukses) {
        JOptionPane.showMessageDialog(this, "Buku berhasil dikembalikan.");
    } else {
        JOptionPane.showMessageDialog(this, "Gagal mengembalikan buku. Pastikan buku sedang dipinjam.");
    }

    loadBukuKeList();
    peminjamanService.hapusPeminjaman(idBuku);

    }//GEN-LAST:event_btnKembalikanBukuActionPerformed

